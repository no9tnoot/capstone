class Results:
    """ Class that holds all results information which will be needed in main.py.
    """
    
    def __init__(self, correct, syntaxOrLogic, tableInfo):
        """ Sets the correct, comment and tableInfo instance variables for an instance of the Results class.
        Calls the getComment method if correct == True. 
        """
        self.correct = correct
        if correct:
            self.comment = ""
        else: 
            self.comment = self.getComment(correct, syntaxOrLogic)
        self.tableInfo = tableInfo
        
    
    def getComment(self, correct, syntaxOrLogic):
        """
        Checks if the studentQuery returns the same output as the modelQuery. Returns an array,
        wherein the first item is a Boolean indicating True if the outputs are identical. If False,
        a second item in the array is a string communicating if the studentQuery is valid and produced
        an incorrect output, or if it is invalid and the server threw an error.
        """
        if syntaxOrLogic == "syn":
            return "The entered query is invalid. Look carefully at your syntax, and ensure names are spelt correctly and exist in the database."
        elif syntaxOrLogic == "log":
            return "The output generated by your query does not match the expected output."
        raise ValueError('The input was incorrect is ' + str(correct) + ', but the syntaxOrLogic var=' + syntaxOrLogic)
        